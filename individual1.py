#!/usr/bin/env python3
# -*- coding: utf-8 -*-

if __name__ == '__main__':
    # Ввести кортеж одной строкой.
    A = tuple(map(int, input("Введите кортеж чисел через пробел: ").split()))
    # Переменная для хранения всех элементов, предшествующих последней такой тройке
    k = []
    # Итерируемся по кортежу, проверяя каждую тройку
    for i in range(len(A) - 2):
        if A[i + 1] > A[i] and A[i + 1] > A[i + 2]:
            k = A[:i]  # Сохраняем все элементы, предшествующие последней такой тройке
            print(f"Тройка {A[i]}, {A[i + 1]}, {A[i + 2]} удовлетворяет условию.")

    # Выводим результат, если он не пустой
    if k:
        print("Элементы, предшествующие последней тройке, где средний элемент больше соседних:")
        print(k)
    else:
        print("Такой тройки в кортеже нет.")